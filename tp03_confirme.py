# XOR
def xor(x,y) :
    '''
    Retourne x xor y (bool)
    x,y: bool
    >>> xor(False,False)
    False
    >>> xor(False,True)
    True
    >>> xor(True,False)
    True
    >>> xor(True,True)
    False
    '''
    return x != y

